package Tarifa;
import java.io.Serializable;

public class Tarifa implements Serializable {

    private double cantidad;

    //------------------------------------------------------------------
    // CONSTRUCTORES
    //------------------------------------------------------------------

    public Tarifa(){
        this.cantidad = 0;
    }

    public Tarifa(double cantidad){
        this.cantidad = cantidad;
    }

    //------------------------------------------------------------------
    // METODOS DE USO
    //------------------------------------------------------------------





    //------------------------------------------------------------------
    // GETTERS
    //------------------------------------------------------------------

    public double getCantidad() {
        return cantidad;
    }


    //------------------------------------------------------------------
    // SETTERS
    //------------------------------------------------------------------


}
